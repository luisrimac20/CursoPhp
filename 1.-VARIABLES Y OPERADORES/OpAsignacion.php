<?php 
    //VARIABLES
    $x = 4.5;
    $y = 5;

    //SUMA DE ASIGNACION
    $x += $y;
    echo $x . '<br>';
    
    //RESTA DE ASIGNACION
    $x -= $y;
    echo $x . '<br>';

    //MULTIPLICACION EN ASIGNACION
    $x *= $y;
    echo $x . '<br>';

    //DIVISION EN ASIGNACION
    $x /= $y;
    echo $x . '<br>';

    //MODULO EN ASIGNACION
    $x %= $y;
    echo $x . '<br>';

?>